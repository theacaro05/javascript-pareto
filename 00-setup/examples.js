// Run this file with:  node 2-examples.js

// console.log prints things to the terminal.
console.log("Hello, JavaScript!");

// You can print numbers, strings, or both at once.
console.log(2 + 2);
console.log("two plus two is", 2 + 2);

// Comments (lines starting with //) are ignored by JavaScript.
// You write them for humans (including future-you).

/*
   This is a multi-line comment.
   Use it when one line isn't enough.
*/

// You can print multiple things by passing them as separate arguments.
console.log("name:", "Jane", "| age:", 36);

// Each console.log makes a new line in the terminal.
console.log("line 1");
console.log("line 2");
